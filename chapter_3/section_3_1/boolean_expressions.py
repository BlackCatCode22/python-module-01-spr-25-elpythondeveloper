# Chapter 3 - Conditional execution
# 3.1 Boolean expressions
# A boolean expression is an expression that is either true or false.
# The equality operator (==) compares two operands and produces True if they are equal and False otherwise
# True and False are special values that belong to the class bool.

# # Example 1 - The equality operator (==)
# Compares the integer value 5 with 5, since they are equal, it produces True
print(5 == 5)
# Compares the integer value 5 with 6, since they are not equal, it produces False
print(5 == 6)

# Example 2 - Display the data type of True and False
# Displays the data type of the value True
print(type(True))
# Displays the data type of the value True
print(type(False))