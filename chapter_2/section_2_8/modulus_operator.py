# Modulus Operator
# The modulus operator works on integers and yields the remainder when the first
# operand is divided by the second.
# The modulus operator is a percent sign %.

# Example 1 - Division
# Divide 7 by 3 and store the result in variable quotient
# 7 divided by 3 is 2 remainder 1. 2 is what is stored in the variable quotient
quotient = 7 // 3
# display the value stored in variable quotient in the console
print(quotient)

# Example 2 - Modulus(remainder)
# Divide 7 by 3 and store the result in variable remainder
# 7 divided by 3 is 2 remainder 1. 1 is what is stored in the variable remainder
remainder = 7 % 3
# display the value stored in variable remainder in the console
print(remainder)