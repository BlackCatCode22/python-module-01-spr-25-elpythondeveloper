# Comments
# In Python comments start with the # symbol:

# This is a single line comments
print('Pearl Jam Rules@')

# This is multi line comment using triple single quotes
'''
This is a multiline comment using triple single quotes. 
It can span multiple lines.
'''
print('Metallica Rules!')

# This is multi line comment using triple double quotes
"""
This is a multiline comment using triple double quotes. 
It can span multiple lines. I use an escape character do display an apostrophe in the string
"""
print('Guns N'' Roses Rules!')